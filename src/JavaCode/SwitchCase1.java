package JavaCode;

public class SwitchCase1 {

    public static void main(String[]args){
        int test=20;
        switch (test){

            case 10:
                System.out.println("test is greater then 10");
                break;
            case 12:
                System.out.println("test is greater then 12");
                break;
            case 15:
                System.out.println("test is greater then 15");
                break;
            case 20:
                System.out.println("test is greater then 20");
                break;
            case 19:
                System.out.println("test is greater then 18");
                break;
             default:
                 System.out.println("test is greater then 19");
        }

    }

}
